package com.myapplication.map.Orders;

public class Location {
    private double lat;

    private double Longi ;

    public void setLat(double lat){
        this.lat = lat;
    }
    public double getLat(){
        return this.lat;
    }
    public void setLong(double Longi){
        this.Longi = Longi;
    }
    public double getLong(){
        return this.Longi;
    }
}
